//Creates whitespace according to level of indent
//indentLvl = amount of white space to be created
//return = amount of white space 
function createIndent(indentLvl){
    let indent = "";
    for (var i = 0;i < indentLvl;i++){
        indent += "    ";
    }
    return indent;
}
